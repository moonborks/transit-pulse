const groupShapePoints = (points: ShapePoint[]): Record<string, [number, number][]> => {
  const grouped: Record<string, [number, number][]> = {};
  for (const p of points) {
    const pts = grouped[p.shapeId] ?? [];
    pts.push([p.lat, p.lon]);
    grouped[p.shapeId] = pts;
  }
  return grouped;
};

