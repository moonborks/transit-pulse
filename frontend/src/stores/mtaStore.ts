export const getShapeColor = (shapeId: string): string => {
  const trip = trips.find((t) => t.shapeId === shapeId);
  if (!trip) return "#888888";
  const route = routes.find((r) => r.id === trip.routeId);
  return route ? `#${route?.color}` : "#888888";
};

